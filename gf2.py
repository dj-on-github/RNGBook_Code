def gmul(a, b, degree=8):
    if degree == 1:
        return gf2mul(a,b)
    else:
        # Get the polynomial
        poly = polys[degree]
        # But we don't want the x^degree term, hack it off.
        poly = poly & ((1 << degree) - 1)
        
        z = 0
        if a & 1:
            r = b
        else:
            r = 0
        for i in xrange(1,degree+1):
            if  (b & (1 << (degree-1))) == 0:
                b = z ^ ((b << 1) & ((1 << degree) - 1))
            else:
                b = poly ^ ((b << 1) & ((1 << degree) - 1))
            if a & (1 << i):
                r = r ^ b
            else:
                r = r ^ z
        return r

